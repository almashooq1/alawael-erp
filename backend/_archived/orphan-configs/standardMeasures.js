/**
 * Seed measures into database (upsert by code)
 */
async function seedStandardMeasures() {
  const mongoose = require('mongoose');
  const Measure = mongoose.model('Measure');
  const results = { created: 0, updated: 0, errors: [] };

  for (const data of standardMeasures) {
    try {
      const existing = await Measure.findOne({ code: data.code });
      if (existing) {
        await Measure.updateOne({ code: data.code }, { $set: data });
        results.updated++;
      } else {
        await Measure.create(data);
        results.created++;
      }
    } catch (err) {
      results.errors.push({ code: data.code, error: err.message });
    }
  }

  return results;
}

module.exports = { standardMeasures, seedStandardMeasures };
