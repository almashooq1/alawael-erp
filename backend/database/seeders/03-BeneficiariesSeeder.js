async function run() {
  console.log('🧒 إنشاء المستفيدين (50 مستفيد)...');

  const riyadh = await Branch.findOne({ code: 'RUH-01' });
  const jeddah = await Branch.findOne({ code: 'JED-01' });
  const dammam = await Branch.findOne({ code: 'DMM-01' });

  if (!riyadh || !jeddah || !dammam) {
    throw new Error('❌ لم يتم العثور على الفروع.');
  }

  const beneficiaries = buildBeneficiaries(riyadh, jeddah, dammam);

  // تعيين branch لكل مستفيد حسب nationalId
  const branchMap = {};
  for (let i = 1; i <= 20; i++) branchMap[`110123400${String(i).padStart(1, '0')}`] = riyadh._id;
  // (سيتم تحديده تلقائياً من خلال address.city)

  let created = 0;
  let updated = 0;

  // تحديد الفرع لكل مستفيد
  const beneficiariesWithBranch = beneficiaries.map(b => {
    let branchId;
    const city = b.address?.city || '';
    if (city === 'الرياض') branchId = riyadh._id;
    else if (city === 'جدة') branchId = jeddah._id;
    else if (city === 'الدمام') branchId = dammam._id;
    else branchId = riyadh._id;

    return { ...b, branch: branchId };
  });

  let mrnCounter = 1;
  for (const benefData of beneficiariesWithBranch) {
    const existing = await Beneficiary.findOne({ nationalId: benefData.nationalId });
    if (existing) {
      await Beneficiary.updateOne(
        { nationalId: benefData.nationalId },
        {
          $set: {
            ...benefData,
            mrn: existing.mrn || `MRN-2024-${String(mrnCounter).padStart(5, '0')}`,
          },
        }
      );
      updated++;
    } else {
      // توليد mrn فريد إذا لم يكن موجوداً
      const mrnValue = `MRN-2024-${String(mrnCounter).padStart(5, '0')}`;
      await Beneficiary.create({ ...benefData, mrn: mrnValue });
      created++;
    }
    mrnCounter++;
  }

  console.log(
    `   ✅ المستفيدون: ${created} تم إنشاؤهم، ${updated} تم تحديثهم (الإجمالي: ${beneficiaries.length})`
  );
  return { created, updated, total: beneficiaries.length };
}

